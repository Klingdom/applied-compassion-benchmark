// ─── KeyTakeaway sub-component ────────────────────────────────────────────────
//
// "If you remember one thing" — compact highlighted callout using verbatim
// first keyFindings entry. Degrades gracefully if text is empty.

function KeyTakeaway({ text }: { text: string }) {
  if (!text.trim()) return null;

  // Parse the same **Bold heading.** Rest format as KeyFinding
  const boldMatch = text.match(/^\*\*([^*]+)\*\*\s*(.*)/);
  const head = boldMatch ? boldMatch[1] : "";
  const body = boldMatch ? boldMatch[2].trim() : text;

  return (
    <div
      role="note"
      aria-label="If you remember one thing"
      className="border-t border-line"
    >
      <div className="max-w-[860px] mx-auto px-4 sm:px-6 lg:px-8 py-6">
        <div className="rounded-[14px] border border-[rgba(125,211,252,0.22)] bg-[rgba(125,211,252,0.05)] px-5 py-4 flex gap-4 items-start">
          {/* Eyebrow label — vertical accent bar */}
          <div
            aria-hidden="true"
            className="shrink-0 w-1 self-stretch rounded-full bg-[rgba(125,211,252,0.45)] mt-0.5"
          />
          <div className="min-w-0">
            <p className="text-[0.68rem] font-bold uppercase tracking-[0.18em] text-accent mb-2">
              If you remember one thing
            </p>
            <p className="text-[0.97rem] leading-relaxed text-text max-w-[68ch]">
              {head && (
                <span className="font-bold">{head} </span>
              )}
              {body && (
                <span className="text-muted">{body}</span>
              )}
            </p>
          </div>
        </div>
      </div>
    </div>
  );
}

// ─── KeyFinding sub-component ─────────────────────────────────────────────────

function KeyFinding({ index, text }: { index: number; text: string }) {
  // text format: "**Bold heading.** Rest of the finding text."
  // We split on the closing ** to extract the bold head and the body.
  // Note: key findings are single-line strings, so /s flag is not needed
  const boldMatch = text.match(/^\*\*([^*]+)\*\*\s*(.*)/);
  const head = boldMatch ? boldMatch[1] : "";
  const body = boldMatch ? boldMatch[2].trim() : text;

  return (
    <li className="flex gap-4 items-start">
      {/* Number badge */}
      <span
        aria-hidden="true"
        className="flex-shrink-0 w-7 h-7 rounded-full border border-[rgba(125,211,252,0.25)] bg-[rgba(125,211,252,0.06)] text-accent text-[0.75rem] font-bold flex items-center justify-center tabular-nums mt-0.5"
      >
        {index}
      </span>
      <div className="min-w-0">
        {head && (
          <span className="font-bold text-text text-[0.95rem]">{head} </span>
        )}
        {body && (
          <span className="text-muted text-[0.92rem] leading-relaxed">
            {body}
          </span>
        )}
      </div>
    </li>
  );
}
